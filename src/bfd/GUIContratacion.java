    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if(dcFecha.getDate()!=null && (Integer) sMonto.getValue() > 0){
            Pago p = new Pago();
            p.setFecha(dcFecha.getDate());
            p.setMonto((Integer)sMonto.getValue());
            p.setContratacion(con);
            p.setVigente(true);
            Conexion.getInstance().persist(p);
            cargarPagos();
            dcFecha.setDate(null);
            sMonto.setValue(0);
            jDialog1.setVisible(false);//no se como cerrar ventanas
        }
        
    }//GEN-LAST:event_btnAceptarActionPerformed

